# Comparison Operators
print("Please enter the first number...")
num1 = int(input())
print()
print("Please enter the second number...")
num2 = int(input())
print()
if (num1 < num2):
    print("The first number is the smallest!")
    print()
elif (num1 > num2):
    print("The second number is the smallest!")
    print()
else:
    print("The two numbers are equal")
    print()